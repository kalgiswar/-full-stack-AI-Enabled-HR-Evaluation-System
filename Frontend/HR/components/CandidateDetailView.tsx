"use client";

import React, { useEffect, useState } from "react";
import { User, FileText, Code, MessageSquare, Play, ShieldAlert, CheckCircle, Download, ArrowLeft, BrainCircuit, Star, AlertCircle } from "lucide-react";
import { cn } from "@/lib/utils";
import Link from "next/link";
import { db } from "@database/firebase/client";
import { doc, getDoc, Timestamp } from "firebase/firestore";

interface CandidateData {
    candidateName: string;
    matchScore: number;
    category: "High Match" | "Potential" | "Reject";
    reasoning: string;
    skillsFound: string[];
    missingSkills: string[];
    experienceSummary: string;
    jobDescription: string;
    createdAt: Timestamp;
}

export default function CandidateDetailView({ candidateId }: { candidateId: string }) {
    const [activeTab, setActiveTab] = useState("overview");
    const [candidate, setCandidate] = useState<CandidateData | null>(null);
    const [loading, setLoading] = useState(true);

    useEffect(() => {
        async function fetchCandidate() {
            try {
                const docRef = doc(db, "resume_analyses", candidateId);
                const docSnap = await getDoc(docRef);
                
                if (docSnap.exists()) {
                    setCandidate(docSnap.data() as CandidateData);
                }
            } catch (error) {
                console.error("Error fetching candidate:", error);
            } finally {
                setLoading(false);
            }
        }
        fetchCandidate();
    }, [candidateId]);

    const handleDownload = () => {
        if (!candidate) return;

        const reportContent = `
=========================================
HIREGUARD AI CANDIDATE REPORT
=========================================
Candidate Name: ${candidate.candidateName}
Match Score:    ${candidate.matchScore}%
Category:       ${candidate.category}

EXPERIENCE SUMMARY:
${candidate.experienceSummary}

SKILLS FOUND:
${candidate.skillsFound.join(', ')}

MISSING SKILLS (VS JD):
${candidate.missingSkills.length > 0 ? candidate.missingSkills.join(', ') : 'None'}

AI REASONING & ANALYSIS:
${candidate.reasoning}

-----------------------------------------
Generated by HireGuard AI on ${new Date().toLocaleString()}
=========================================
        `;

        const blob = new Blob([reportContent], { type: 'text/plain' });
        const url = window.URL.createObjectURL(blob);
        const link = document.createElement('a');
        link.href = url;
        link.download = `${candidate.candidateName.replace(/\s+/g, '_')}_AI_Report.txt`;
        document.body.appendChild(link);
        link.click();
        document.body.removeChild(link);
        window.URL.revokeObjectURL(url);
    };

    if (loading) {
        return (
            <div className="flex flex-col items-center justify-center min-h-[600px] gap-4">
                <div className="w-16 h-16 border-4 border-primary-200/20 border-t-primary-200 rounded-full animate-spin"></div>
                <p className="text-light-100 animate-pulse">Analyzing Candidate Profile...</p>
            </div>
        );
    }

    if (!candidate) {
        return (
            <div className="flex flex-col items-center justify-center min-h-[600px] gap-6 text-center">
                <div className="w-20 h-20 bg-destructive-100/10 rounded-full flex items-center justify-center text-destructive-100">
                    <AlertCircle size={40} />
                </div>
                <div>
                    <h2 className="text-2xl font-bold text-white">Candidate Not Found</h2>
                    <p className="text-light-100 mt-2 max-w-sm">We couldn't find the record you were looking for. It might have been deleted or the ID is incorrect.</p>
                </div>
                <Link href="/hr/candidates" className="bg-white text-dark-100 px-8 py-3 rounded-xl font-bold hover:bg-gray-100 transition-all">
                    Go Back to Candidates
                </Link>
            </div>
        );
    }

    return (
        <div className="flex flex-col gap-8 animate-fadeIn max-w-7xl mx-auto">
            {/* Breadcrumb & Navigation */}
            <div className="flex items-center justify-between">
                <Link href="/hr/candidates" className="flex items-center gap-2 text-light-100 hover:text-white transition-colors font-medium">
                    <ArrowLeft size={18} /> Back to Pipeline
                </Link>
                <div className="flex gap-3">
                    <button 
                        onClick={handleDownload}
                        className="px-5 py-2 rounded-xl bg-white/5 border border-white/10 text-light-100 font-bold hover:bg-white/10 transition-all flex items-center gap-2"
                    >
                        <Download size={18} /> Resume
                    </button>
                    <button className="px-5 py-2 rounded-xl bg-primary-200 text-white font-bold shadow-[0_0_20px_rgba(130,103,248,0.3)] hover:scale-105 transition-all flex items-center gap-2">
                         Move to Next Stage
                    </button>
                </div>
            </div>

            {/* Profile Header Card */}
            <div className="relative overflow-hidden group">
                {/* Background decorative element */}
                <div className="absolute top-0 right-0 w-96 h-96 bg-primary-200/5 rounded-full blur-[100px] -mr-48 -mt-48"></div>
                
                <div className="card p-8 md:p-10 border border-white/10 bg-dark-200/40 backdrop-blur-xl relative z-10">
                    <div className="flex flex-col md:flex-row gap-10 items-start md:items-center">
                        {/* Avatar */}
                        <div className="relative">
                            <div className="w-32 h-32 rounded-3xl bg-gradient-to-br from-primary-200 to-purple-600 flex items-center justify-center text-5xl font-bold text-white shadow-2xl rotate-3 group-hover:rotate-0 transition-transform duration-500">
                                {candidate.candidateName.split(' ').map(n => n[0]).join('').substring(0,2)}
                            </div>
                            <div className="absolute -bottom-2 -right-2 w-12 h-12 rounded-2xl bg-dark-100 border-2 border-primary-200 flex items-center justify-center shadow-lg">
                                <BrainCircuit className="text-primary-200 animate-pulse" size={24} />
                            </div>
                        </div>

                        {/* Basic Info */}
                        <div className="flex-1 space-y-3">
                            <div className="flex flex-wrap items-center gap-4">
                                <h1 className="text-4xl font-extrabold text-white tracking-tight">{candidate.candidateName}</h1>
                                <span className={cn(
                                    "px-4 py-1.5 rounded-full text-xs font-bold border",
                                    candidate.category === "High Match" ? "bg-success-100/10 text-success-100 border-success-100/20" :
                                    candidate.category === "Reject" ? "bg-destructive-100/10 text-destructive-100 border-destructive-100/20" :
                                    "bg-primary-200/10 text-primary-200 border-primary-200/20"
                                )}>
                                    {candidate.category.toUpperCase()}
                                </span>
                            </div>
                            <p className="text-xl text-light-100 flex items-center gap-2 italic">
                                <Briefcase size={20} className="text-primary-200" />
                                {candidate.jobDescription.split('\n')[0].substring(0, 100)}...
                            </p>
                            <div className="flex flex-wrap gap-2 pt-2">
                                {candidate.skillsFound.map(skill => (
                                    <span key={skill} className="px-3 py-1 bg-white/5 border border-white/10 rounded-lg text-xs font-medium text-light-100">
                                        {skill}
                                    </span>
                                ))}
                            </div>
                        </div>

                        {/* Score Circle */}
                        <div className="flex flex-col items-center justify-center p-6 bg-white/5 rounded-3xl border border-white/10 backdrop-blur-sm min-w-[180px]">
                            <span className="text-xs font-bold text-light-100/50 uppercase tracking-widest mb-2">Match Score</span>
                            <div className="relative w-24 h-24 flex items-center justify-center">
                                <svg className="w-full h-full -rotate-90">
                                    <circle cx="48" cy="48" r="44" stroke="currentColor" strokeWidth="8" fill="transparent" className="text-white/5" />
                                    <circle 
                                        cx="48" cy="48" r="44" stroke="currentColor" strokeWidth="8" fill="transparent" 
                                        strokeDasharray={276}
                                        strokeDashoffset={276 - (276 * candidate.matchScore) / 100}
                                        className={cn("transition-all duration-1000 ease-out", candidate.matchScore >= 80 ? "text-success-100" : candidate.matchScore >= 50 ? "text-yellow-400" : "text-destructive-100")} 
                                    />
                                </svg>
                                <span className="absolute text-2xl font-black text-white">{candidate.matchScore}%</span>
                            </div>
                        </div>
                    </div>
                </div>
            </div>

            {/* Navigation Tabs */}
            <div className="flex overflow-x-auto gap-2 bg-dark-200/30 p-1.5 rounded-2xl border border-white/5 scrollbar-hide">
                <TabItem id="overview" label="Analysis Overview" icon={<Star size={18} />} active={activeTab === "overview"} onClick={setActiveTab} />
                <TabItem id="skills" label="Skill Breakdown" icon={<Code size={18} />} active={activeTab === "skills"} onClick={setActiveTab} />
                <TabItem id="interview" label="AI Interview" icon={<MessageSquare size={18} />} active={activeTab === "interview"} onClick={setActiveTab} />
                <TabItem id="proctor" label="Integrity Log" icon={<ShieldAlert size={18} />} active={activeTab === "proctor"} onClick={setActiveTab} />
            </div>

            {/* Content Section */}
            <div className="min-h-[400px]">
                {activeTab === "overview" && (
                    <div className="grid grid-cols-1 lg:grid-cols-2 gap-8">
                        <div className="space-y-8">
                            <div className="space-y-4">
                                <h3 className="text-2xl font-bold text-white flex items-center gap-3">
                                    <BrainCircuit className="text-primary-200" />
                                    AI Perception
                                </h3>
                                <div className="card p-6 bg-primary-200/5 border border-primary-200/10 rounded-2xl">
                                    <p className="text-light-100 leading-relaxed italic text-lg whitespace-pre-wrap">
                                        "{candidate.reasoning}"
                                    </p>
                                </div>
                            </div>
                            
                            <div className="space-y-4">
                                <h3 className="text-2xl font-bold text-white">Experience Summary</h3>
                                <div className="card p-6 border border-white/5 rounded-2xl">
                                    <p className="text-light-100 leading-relaxed">
                                        {candidate.experienceSummary}
                                    </p>
                                </div>
                            </div>
                        </div>

                        <div className="card-border">
                            <div className="card p-0 overflow-hidden h-full flex flex-col">
                                <div className="p-6 border-b border-white/5 bg-white/5">
                                    <h3 className="text-xl font-bold text-white">Resume Content</h3>
                                </div>
                                <div className="p-8 flex-1 overflow-y-auto max-h-[500px] font-mono text-sm text-light-100/70 bg-[#0a0a14] leading-loose">
                                    {/* Showing part of JD as mock content since we don't store full resume text usually */}
                                    <div className="space-y-4">
                                        <p className="text-primary-200 font-bold uppercase tracking-widest text-[10px]">Candidate Profile extracted</p>
                                        <div className="space-y-2">
                                            <p className="font-bold text-white">Contact:</p>
                                            <p>{candidate.candidateName.toLowerCase().replace(' ', '.')}@email.com â€¢ +1 (555) 000-0000</p>
                                        </div>
                                        <div className="space-y-2">
                                            <p className="font-bold text-white">Summary:</p>
                                            <p>{candidate.experienceSummary}</p>
                                        </div>
                                        <div className="space-y-2">
                                            <p className="font-bold text-white">Skills Found:</p>
                                            <p>{candidate.skillsFound.join(', ')}</p>
                                        </div>
                                    </div>
                                </div>
                            </div>
                        </div>
                    </div>
                )}

                {activeTab === "skills" && (
                    <div className="grid grid-cols-1 md:grid-cols-2 gap-8">
                         <div className="space-y-6">
                            <h3 className="text-xl font-bold text-white flex items-center gap-2">
                                <CheckCircle className="text-success-100" /> Found Skills
                            </h3>
                            <div className="flex flex-wrap gap-4">
                                {candidate.skillsFound.map(skill => (
                                    <div key={skill} className="card p-4 border border-success-100/20 bg-success-100/5 rounded-xl flex items-center gap-3 min-w-[140px] group hover:bg-success-100/10 transition-all">
                                        <div className="w-8 h-8 rounded-lg bg-success-100/20 flex items-center justify-center text-success-100">
                                            <Code size={16} />
                                        </div>
                                        <span className="font-bold text-white">{skill}</span>
                                    </div>
                                ))}
                            </div>
                        </div>

                        <div className="space-y-6">
                            <h3 className="text-xl font-bold text-white flex items-center gap-2">
                                <AlertCircle className="text-destructive-100" /> Missing from JD
                            </h3>
                            <div className="flex flex-wrap gap-4">
                                {candidate.missingSkills.length > 0 ? candidate.missingSkills.map(skill => (
                                    <div key={skill} className="card p-4 border border-destructive-100/20 bg-destructive-100/5 rounded-xl flex items-center gap-3 min-w-[140px] group hover:bg-destructive-100/10 transition-all">
                                         <div className="w-8 h-8 rounded-lg bg-destructive-100/20 flex items-center justify-center text-destructive-100">
                                            <AlertCircle size={16} />
                                        </div>
                                        <span className="font-bold text-white">{skill}</span>
                                    </div>
                                )) : <p className="text-light-100/50 italic">No critical skills missing based on JD analysis.</p>}
                            </div>
                        </div>
                    </div>
                )}

                {activeTab === "interview" && (
                    <div className="card-border">
                         <div className="card p-20 flex flex-col items-center justify-center text-center gap-6 bg-gradient-to-br from-primary-200/5 to-purple-600/5">
                            <div className="w-24 h-24 rounded-3xl bg-primary-200/10 flex items-center justify-center text-primary-200 shadow-inner">
                                <MessageSquare size={48} />
                            </div>
                            <div>
                                <h3 className="text-2xl font-bold text-white">Interview Intelligence</h3>
                                <p className="text-light-100 mt-2 max-w-md">The interview analysis will be available once the candidate completes the AI voice assessment.</p>
                            </div>
                            <button className="bg-primary-200 text-white px-8 py-3 rounded-xl font-bold shadow-lg hover:bg-primary-200/90 transition-all">
                                Generate Invitation Link
                            </button>
                        </div>
                    </div>
                )}

                {activeTab === "proctor" && (
                     <div className="card-border">
                        <div className="card p-20 flex flex-col items-center justify-center text-center gap-6">
                            <ShieldAlert size={64} className="text-light-100/20" />
                            <div>
                                <h3 className="text-2xl font-bold text-white">Integrity Guard</h3>
                                <p className="text-light-100 mt-2">No integrity violations detected during the submission phase.</p>
                            </div>
                        </div>
                    </div>
                )}
            </div>
        </div>
    );
}

function TabItem({ id, label, icon, active, onClick }: { id: string, label: string, icon: React.ReactNode, active: boolean, onClick: (id: string) => void }) {
    return (
        <button 
            onClick={() => onClick(id)}
            className={cn(
                "flex items-center gap-2 px-6 py-3 rounded-xl transition-all font-bold whitespace-nowrap",
                active 
                ? "bg-primary-200 text-white shadow-xl" 
                : "text-light-100 hover:bg-white/5 hover:text-white"
            )}
        >
            {icon}
            {label}
        </button>
    );
}

const Briefcase = ({ size, className }: { size?: number, className?: string }) => (
    <svg 
        xmlns="http://www.w3.org/2000/svg" 
        width={size || 24} 
        height={size || 24} 
        viewBox="0 0 24 24" 
        fill="none" 
        stroke="currentColor" 
        strokeWidth="2" 
        strokeLinecap="round" 
        strokeLinejoin="round" 
        className={className}
    >
        <rect width="20" height="14" x="2" y="7" rx="2" ry="2"/>
        <path d="M16 21V5a2 2 0 0 0-2-2h-4a2 2 0 0 0-2 2v16"/>
    </svg>
);

